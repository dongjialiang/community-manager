<template>
  <input
    v-if="column?.type === 'bool'"
    type="checkbox"
    class="checkbox"
    :value="item[column.name]"
    :checked="item[column.name]"
    @click="changeListItem(id, column, item)"
  >

  <template v-else-if="column?.type === 'file'">
    <img
      v-if="item[column.name]"
      class="banner-img"
      :src="item[column.name]"
    >
    <template v-else>
      <input
        id="banner"
        type="file"
        name="banner"
        hidden
        @change="modalAction(uploadImage, '上传海报', id)"
      >
      <label
        class="banner"
        for="banner"
      >上传🖼海报</label>
    </template>
  </template>

  <template
    v-for="option in column?.options"
    v-else-if="column?.type === 'radio'"
    :key="option"
  >
    <input
      type="radio"
      :name="`radio${item._id || 'new'}`"
      :value="option"
      :checked="item[column.name] === option"
      @change="changeListItem(id, column, item)"
    >{{ option }}
  </template>

  <select
    v-else-if="column?.type === 'select'"
    :value="item[column.name]"
    @input="changeListItem(id, column, item)"
  >
    <option
      v-for="option in column?.options"
      :key="option"
      :value="option"
    >
      {{ option }}
    </option>
  </select>

  <div
    v-else-if="column.type === 'array'"
    class="array-item"
  >
    <div class="array-item-title">
      <i>查询多个</i>
      <i>查询</i>
      <i>创建</i>
      <i>创建多个</i>
      <i>更新</i>
      <i>删除</i>
      <i>删除多个</i>
    </div>
    <div
      v-for="(authNum, authKey) in item[column.name]"
      :key="authKey"
    >
      <span class="array-item-name">{{ authKey }}</span>
      <div
        v-for="(authVal, index) in getAuthArr(authNum)"
        :key="index"
        class="array-item-value"
      >
        <input
          type="checkbox"
          :value="authVal"
          :checked="authVal === (2 ** index)"
          @click="changeListItem(id, column, item, authKey, 2 ** index)"
        >
      </div>
    </div>
  </div>

  <span
    v-else-if="column.type === 'bool'"
    @input="changeListItem(id, column, item)"
  >
    {{ item[column.name] }}
  </span>
  <span
    v-else
    contenteditable="plaintext-only"
    @input="changeListItem(id, column, item)"
  >
    {{ item[column.name] }}
  </span>
</template>

<script>
import { inject, watchEffect } from 'vue'
import modal from '../services/modal'
import Upload from '../services/Upload'
export default {
  name: 'DataAction',
  props: {
    id: {
      type: String,
      default: undefined
    },
    sourceSymbol: {
      type: Symbol,
      required: true
    },
    tempListSymbol: {
      type: Symbol,
      required: true
    },
    column: {
      type: Object,
      required: true
    },
    item: {
      type: Object,
      required: true
    },
  },
  setup(props) {
    const source   = inject(props.sourceSymbol)
    const tempList = inject(props.tempListSymbol)

    const modalAction = modal().modalAction

    // 修改列表内容(只影响视图层, 不修改模型层)
    const changeListItem = (id, column, item, path, fixAuthNum) => {
      let content;
      const e = event;
      const name = column.name
      const type = column.type
      if (type === 'bool') {
        e.currentTarget.value =
                  e.currentTarget.value === 'true'
                    ? false : true
        content = e.currentTarget.value
      } else if (type === 'file') {
        content = path
      } else if (type === 'select' || type === 'radio') {
        content = e.currentTarget.value
      } else if (type === 'array') {
        const eNum = Number(e.currentTarget.value)
        let tempListVal = tempList.value[id]
        const authList = tempListVal !== undefined
          && tempListVal.hasOwnProperty(name)
          ? tempListVal[name]
          : { ...item[name] }
        const newNum = e.currentTarget.checked ? fixAuthNum : -eNum
        authList[path] = authList[path] + newNum
        content = authList
      } else {
        content = e.currentTarget.innerText
      }
      const tempObj = {} // 临时对象

      const itemIsNone = item[name] === undefined

      if ((content === '' && itemIsNone)
        || compare(content, item[name])) {
        delete tempList.value[id][name]
      } else {
        tempObj[name] = content
        if (tempList.value[id]) {
          tempList.value[id] = {
            ...tempList.value[id],
            ...tempObj
          }
        } else {
          tempList.value[id] = tempObj
        }
      }
    }
    // 上传图片
    const uploadImage = async (id) => {
      const formData = new FormData()
      const bannerDom = await document
        .querySelector('input[name=\'banner\']')
      await formData.append('banner', bannerDom.files[0])

      const res = await Upload.imageUpload('image', formData)
      if (res.status === 200) {
        for (const data of source.value.data) {
          if (data._id === id) {
            data.banner = res.data.path
            changeListItem(id, props.column, data, data.banner)
            break
          }
        }
      }
    }

    const compare = (first, second) => {
      if (typeof first === 'object' && typeof second === 'object') {
        return JSON.stringify(first) === JSON.stringify(second)
      } else {
        return first === second
      }
    }

    const getAuthArr = (authNum) => {
      const authArr = []
      let n = 64
      while (n > 0) {
        if (authNum >= n) {
          authNum -= n
          authArr.unshift(n)
        } else {
          authArr.unshift(0)
        }
        n = n / 2 | 0
      }
      return authArr
    }
    return { uploadImage, changeListItem, modalAction, getAuthArr }
  }
}
</script>

